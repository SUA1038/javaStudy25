package ch01;

public class LongExam {

	public static void main(String[] args) {
		long var1 = 10 ;
		long var2 = 20L ;
		// long var3 = 1000000000000000; L을 붙여야 처리가 된다.
		long var4 = 100000000000000000L ;
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var4);
		
		// Long 타입은 변수를 초기화할 때 정수값 뒤에 소문자 l, 대문자 L을 붙인다.
		// 일반적으로 대문자 L을 더 많이 사용한다.
		

	}

}

package ch01;

public class RunStateExam {

	public static void main(String[] args) {
		
		int x = 1 ; // 정수타입 x 변수에 1을 넣는다.
		int y = 2 ; // 정수타입 y 변수에 2를 넣는다.
		int result = x + y ; 
		// 정수타입 result 변수에 x 변수와 y 변수를 더한 값을 넣는다.
		
		System.out.println("x + y = " + result);
		
	} // main 메서드 종료
	
} // 클래스 종료
